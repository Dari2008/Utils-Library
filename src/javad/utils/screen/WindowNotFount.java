package javad.utils.screen;

public class WindowNotFount extends Exception {

	public WindowNotFount() {
	}

	public WindowNotFount(String message) {
		super(message);
	}

	public WindowNotFount(Throwable cause) {
		super(cause);
	}

	public WindowNotFount(String message, Throwable cause) {
		super(message, cause);
	}

	public WindowNotFount(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
